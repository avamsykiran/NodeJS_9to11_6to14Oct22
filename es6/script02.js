console.log(45!="45");
console.log(5>2);
console.log(5<2);
console.log(5>=2);
console.log(5<=2);

//Logical Operators
//&& - and
//|| - or

//Ternary Pair
console.log("Out of 12 and -12, " + (12>-12?-12:12) + " is 'greater'")
console.log('Out of 12 and -12, ' + (12>-12?12:-12) + ' is "lower"')